#include "../so_long.h"

int	ft_print_nbr(int d)
{
	int	len;

	len = 0;
	ft_putnbr_fd(d, 1);
	if (d <= 0)
		len++;
	while (d != 0)
	{
		len += 1;
		d = d / 10;
	}
	return (len);
}
